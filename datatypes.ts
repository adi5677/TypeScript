//Primitive Data Types

// number 

let age: number = 30;
console.log(age); // 30

//string

let personName: string = "Aditya";
console.log(personName); // Aditya

//boolean

let isStudent: boolean = true;
console.log(isStudent); // true

//null

let emptyValue: null = null;
console.log(emptyValue); // null

// undefined

let value: undefined = undefined;
console.log(value); // undefined

//bigInt

let bigIntValue: bigint = 1234567890123456789012345678901234567890n;
console.log(bigIntValue); // 1234567890123456789012345678901234567890n

//symbol

let sym1: symbol = Symbol("description");
// let sym2: symbol = Symbol("description");
console.log(sym1 === sym2); // false
